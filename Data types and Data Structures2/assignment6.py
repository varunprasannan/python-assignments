# 6. Print values from a nested dictionary

nested_dict = {
  "child1" : {
    "name" : "Rakesh",
    "year" : 2000
  },
  "child2" : {
    "name" : "Samiksha",
    "year" : 2002
  },
  "child3" : {
    "name" : "Ajay",
    "year" : 2003
  },
  "child4" : {
    "name" : "Varun",
    "year" : 2004
  }
}

print(nested_dict["child1"]["name"])
print(nested_dict["child1"]["year"])
print(nested_dict["child2"]["name"])
print(nested_dict["child2"]["year"])
print(nested_dict["child1"]["name"])
